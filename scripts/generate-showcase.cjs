#!/usr/bin/env node
/**
 * Auto-generates src/data/showcase.ts by scanning public/models/ and public/images/.
 *
 * Convention:
 *   - Drop a .glb into public/models/ and a matching .jpg into public/images/
 *   - Filename becomes the slug and display name
 *   - Example: "UEFA-Womens-Euro-Trophy.glb" → slug: "uefa-womens-euro-trophy"
 *                                              → name: "UEFA Womens Euro Trophy"
 *
 * Special files:
 *   - SheenChair.glb is the dev test model (uses polishedGold preset, not original materials)
 *
 * Usage:
 *   node scripts/generate-showcase.cjs
 *   npm run generate-showcase
 *
 * Run this after adding/removing models from public/models/.
 */

const fs = require('fs');
const path = require('path');

const modelsDir = path.join(__dirname, '..', 'public', 'models');
const imagesDir = path.join(__dirname, '..', 'public', 'images');
const outputFile = path.join(__dirname, '..', 'src', 'data', 'showcase.ts');

// Dev test models that use code-applied metal presets instead of C4D materials
const DEV_MODELS = {
  'SheenChair.glb': {
    name: 'Development Test Model',
    description: 'SheenChair.glb used for rendering validation during development.',
    materials: "{ primary: 'polishedGold' }",
    useOriginalMaterials: false,
  },
};

function filenameToSlug(filename) {
  return filename
    .replace(/\.glb$/, '')
    .replace(/[_\s]+/g, '-')
    .toLowerCase();
}

function filenameToName(filename) {
  return filename
    .replace(/\.glb$/, '')
    .replace(/[-_]+/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase());
}

function findThumbnail(basename) {
  const extensions = ['.jpg', '.jpeg', '.png', '.webp'];
  for (const ext of extensions) {
    const imgPath = path.join(imagesDir, basename + ext);
    if (fs.existsSync(imgPath)) {
      return `/images/${basename}${ext}`;
    }
  }
  return '/images/placeholder.jpg';
}

// Scan models directory
const glbFiles = fs.readdirSync(modelsDir)
  .filter(f => f.endsWith('.glb'))
  .sort();

if (glbFiles.length === 0) {
  console.error('No .glb files found in public/models/');
  process.exit(1);
}

// Generate trophy entries
const entries = glbFiles.map(file => {
  const basename = file.replace(/\.glb$/, '');
  const slug = filenameToSlug(file);
  const thumbnailPath = findThumbnail(basename);

  if (DEV_MODELS[file]) {
    const dev = DEV_MODELS[file];
    return `  {
    slug: '${slug}',
    name: '${dev.name}',
    description: '${dev.description}',
    modelPath: '/models/${file}',
    thumbnailPath: '${thumbnailPath}',
    dimensions: { heightMm: 300, widthMm: 200, depthMm: 200 },
    materials: ${dev.materials},
  }`;
  }

  const name = filenameToName(file);
  return `  {
    slug: '${slug}',
    name: '${name}',
    description: '',
    modelPath: '/models/${file}',
    thumbnailPath: '${thumbnailPath}',
    dimensions: { heightMm: 470, widthMm: 200, depthMm: 200 },
    materials: { primary: 'polishedSilver' },
    useOriginalMaterials: true,
  }`;
});

const output = `import type { PublicTrophy } from '../types/index.ts'

// Auto-generated by scripts/generate-showcase.cjs
// Run: npm run generate-showcase

export const SHOWCASE_TROPHIES: PublicTrophy[] = [
${entries.join(',\n')}
]
`;

fs.writeFileSync(outputFile, output);
console.log(`Generated showcase.ts with ${glbFiles.length} trophies:`);
glbFiles.forEach(f => console.log(`  - ${f}`));
